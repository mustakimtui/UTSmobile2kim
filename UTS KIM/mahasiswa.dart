class Mahasiswa {
  String _name, _info;
  int nim;

  String getName() {
    return this._name;
  }

  void setName(String name) {
    this._name = name;
  }

  int getNim() {
    return this.nim;
  }

  void setNim(int nim) {
    this.nim = nim;
  }

  String getInfo() {
    return this._info;
  }

  void setInfo(String info) {
    this._info = info;
  }
}
